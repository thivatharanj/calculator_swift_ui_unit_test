import Foundation

class calMaths {
    
    func addNumbers(x:Int, y:Int) -> Int{
        return x+y
    }
    func subNumbers(x:Int, y:Int) -> Int{
        return x-y
    }
    func divNumbers(x:Int, y:Int) -> Int{
        return x/y
    }
    func multiplyNumbers(x:Int, y:Int) -> Int{
        return x*y
    }
}

